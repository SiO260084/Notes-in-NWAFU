    <h1>研究背景和意义</h1>

    <h2>为什么要研究？</h2>

    <pre class="mermaid">
        flowchart LR
            中国水果的国际竞争力低-->同矮砧种植技术果品不如外国-->土壤有机质含量偏低-->果园土壤有机质含量偏低
            土壤有机质含量偏低-->化学肥料的过度使用-->有机质含量影响果品-->施有机肥-->课题组先前施肥机存在堵塞问题-->解决堵塞问题
            果园土壤有机质含量偏低-->有机质含量影响果品
    </pre>

    <ol>
        <li>我国是水果生产大国，却不是水果生产强国；国际影响力弱、竞争力弱行业标准水平低；生产高品优质水果对我国水果产业发展意义重大。</li>
        <li>同样采用矮砧密植，我国苹果单产及果品品质较国外一些国家仍具有明显差距，造成这一现象的原因很大程度上与果树生长的土壤环境以及果树对养分的吸收有关。</li>
        <li>土壤是果树生长的重要载体，土壤有机质是土壤固相的重要组成部分，是直接决定土壤肥力的物质基础。而我国果园土壤有机质含量普遍偏低。因此，土壤有机质含量低已经成为了制约我国水果产业可持续发展的重要原因。</li>
        <li>我国水果产业单产产量及果品品质偏低的另一重要原因是化学肥料的过度使用。因此，减少化学肥料施用，采用科学合理的施肥技术，已成为我国水果产业提质增效和可持续发展的重要途径。</li>
        <li>土壤有机质在果树优质高效生产中具有重要作用，提高果园有机质含量不仅能实现稳产高产，还能提高果品品质。</li>
        <li>果园环状沟施肥是果树最为理想的施肥方式。然而，在现有研究阶段，可适用于果园环沟施肥方式的有机肥开沟施肥装备仍处于空白，高效施肥装备的缺门断档成为了制约我国水果产业高效发展的瓶颈问题。</li>
        <li>本课题组先前的施肥机（<cite><a
                    href="https://doi.org/10.27409/d.cnki.gxbnu.2020.000732">矮砧果园有机肥料开沟施肥装置设计与试验_臧家俊</a></cite>）存在肥料堵塞的问题。
        </li>
    </ol>

    <h2>解决了什么问题？</h2>

    <ol>
        <li>螺旋式曲线开沟混肥装置高速旋转的排肥管内有机肥堵塞机理尚不清楚的。</li>
        <li>设计螺旋式曲线开沟混肥装置有机肥排肥防堵结构。</li>
        <li>通过离散元仿真研究对防堵结构进一步优化设计，探究防堵结构对排肥性能的影响机制和施肥作业过程中排肥性能改变对肥-土混合性能的影响。</li>
    </ol>

